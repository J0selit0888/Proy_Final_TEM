package Modelos;

public class Usuario {
    
    int id;
    int ci;
    String nombre, correo, estado, password, rol;

    public Usuario() {
    }

    public Usuario(int id, int ci, String nombre, String correo, String estado, String password, String rol) {
        this.id = id;
        this.ci = ci;
        this.nombre = nombre;
        this.correo = correo;
        this.estado = estado;
        this.password = password;
        this.rol = rol;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCi() {
        return ci;
    }

    public void setCi(int ci) {
        this.ci = ci;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
    
    

    
}
